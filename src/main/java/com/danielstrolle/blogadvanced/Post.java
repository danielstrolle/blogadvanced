package com.danielstrolle.blogadvanced;

public class Post {
    private String title;
    private String body;
    private Long id;

    public String getTitle() {
        return title;
    }

    public void setTitle() {
        this.title = title;
    }

    public String getBody() {
        return body;
    }

    public void setBody() {
        this.body = body;
    }

    public Long getId() {
        return id;
    }
    public void setId(Long id) {
        this.id = id;
    }
}
